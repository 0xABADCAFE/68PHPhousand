declare(strict_types=1);

namespace ABadCafe\G8PHPhousand\Processor;
use \Exception;

/**
 *
 */
class Halted extends Exception
{
    public int  $iImmediate = 0;

    /**
     * Raise this fault.
     */
    public function raise(int $iImmediate): bool
    {
        $this->iImmediate = $iImmediate;
        throw $this;
        return false;
    }
}
