declare(strict_types=1);

namespace ABadCafe\G8PHPhousand;

/**
 * Root interface for devices.
 */
interface IDevice
{
    /**
     * Returns a descriptive name of the device for debugging etc.
     */
    public function getName(): string;

    /**
     * Trigger a soft reset of the device. Depending on what the device is, this may leave some state intact.
     */
    public function softReset(): self;

    /**
     * Trigger a hard reset of the device. This should reset the device to some equivalent of a power cycle.
     */
    public function hardReset(): self;
}

